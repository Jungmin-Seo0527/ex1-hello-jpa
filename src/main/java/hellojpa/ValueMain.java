package hellojpa;

public class ValueMain {

    public static void main(String[] args) {
        int a = 10;
        int b = a;

        a = 20;
        System.out.println("a = " + a);
        System.out.println("b = " + b);

        Integer aa = 10;
        Integer bb = aa;

        aa = 20;
        System.out.println("aa = " + aa);
        System.out.println("bb = " + bb);

        String aaa = "abcdefg";
        String bbb = aaa;

    }
}
